fun main() {
    //Kotlin For Loop
    //To loop through array elements,
// use the for loop together with the in operator:
    val myGrades = arrayOf('A', 'C' , 'B' , 'D')
    for(x in myGrades){
        println(x)
    }

    val num = arrayOf(1,3,5,7,8)
    for(a in num){
        println(a)
    }




}